#include<iostream>

using namespace std;

int main(){
    // this sizeof function helps to find the size of the variable such as int, float, char, etc....
    cout<<"size of charecter: "<<sizeof(char)<<"byte"<<endl;
    cout<<"size of intrger: "<<sizeof(int)<<"bytes"<<endl;
    cout<<"size of float: "<<sizeof(float)<<"bytes"<<endl;
    cout<<"size of double: "<<sizeof(double)<<"bytes"<<endl;

    cout <<"hello world";

    return 0;
}